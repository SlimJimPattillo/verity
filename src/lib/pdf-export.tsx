// Create Document Component
export const ReportPDF = ({
  title,
  dateRange,
  narrative,
  metrics,
  organizationName,
  primaryColor,
  financials,
}: ReportPDFProps) => (
  <Document>
    <Page size="A4" style={styles.page}>
      {/* Header */}
      <View style={styles.header}>
        <Text style={styles.title}>{title}</Text>
        <Text style={styles.dateRange}>{dateRange}</Text>
      </View>

      {/* Narrative Section */}
      {narrative && (
        <View style={styles.section}>
          <Text style={styles.sectionTitle}>Our Impact Story</Text>
          <Text style={styles.narrative}>{narrative}</Text>
        </View>
      )}

      {/* Metrics Section */}
      {metrics.length > 0 && (
        <View style={styles.section}>
          <Text style={styles.sectionTitle}>Key Metrics</Text>
          <View style={styles.metricsGrid}>
            {metrics.map((metric, index) => (
              <View key={index} style={styles.metricCard}>
                <Text style={styles.metricValue}>
                  {metric.unit === '$' && '$'}
                  {metric.value.toLocaleString()}
                  {metric.unit === '%' && '%'}
                </Text>
                <Text style={styles.metricLabel}>{metric.label}</Text>
                <Text style={styles.metricType}>{metric.type}</Text>
              </View>
            ))}
          </View>
        </View>
      )}

      {/* Financials Section */}
      {financials && (
        <View style={styles.section}>
          <Text style={styles.sectionTitle}>Financial Allocation</Text>
          <View style={{ marginTop: 8 }}>
            <Text style={{ fontSize: 10, color: '#374151', marginBottom: 4 }}>
              Program Services: {financials.program}%
            </Text>
            <Text style={{ fontSize: 10, color: '#374151', marginBottom: 4 }}>
              Administrative: {financials.admin}%
            </Text>
            <Text style={{ fontSize: 10, color: '#374151', marginBottom: 4 }}>
              Fundraising: {financials.fundraising}%
            </Text>
          </View>
        </View>
      )}

      {/* Footer */}
      <View style={styles.footer}>
        <Text>
          Generated by {organizationName} â€¢ Created with Verity Impact Reports
        </Text>
      </View>
    </Page>
  </Document>
);

/**
 * Generate and download a PDF report
 */
export async function generatePDF(props: ReportPDFProps): Promise<void> {
  try {
    const blob = await pdf(<ReportPDF {...props} />).toBlob();
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = `${props.title.replace(/\s+/g, '-').toLowerCase()}.pdf`;
    link.click();
    URL.revokeObjectURL(url);
  } catch (error) {
    console.error('Error generating PDF:', error);
    throw new Error('Failed to generate PDF');
  }
}

